public class ComparacionFloatDouble {
    public static void main(String[] args) {
        float numeroFloat = 3.14159265358979323846f; // Valor truncado a 7 dígitos decimales de precisión
        double numeroDouble = 3.14159265358979323846; // Valor con precisión completa

        System.out.println("Valor de float: " + numeroFloat); // Salida: 3.1415927
        System.out.println("Valor de double: " + numeroDouble); // Salida: 3.141592653589793
    }
}
